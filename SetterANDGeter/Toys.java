class Toys
{
    static String toysName ;
	static String toysColor ;
	static int toysPrice ;
	
	static void setToysName(String nm)
	{
	   toysName = nm ;
	}
	static String getToysName()
	{
	   return toysName ;
	}
	
	static void setToysColor(String co)
	{
	   toysColor = co ;
	}
	static String getToysColor()
	{
	  return toysColor ;
	}
	
	static void setToysPrice(int pr)
	{
	   toysPrice = pr ;
	}
	static int getToysPrice()
	{
		return toysPrice ;
	}
}