class Furniture
{
	static String furnitureType ;
	static String furnitureColor ;
	static int furniturePrice ;
	
	static void setFurnitureType(String type)
	{
		furnitureType = type ;
	}
	static String getFurnitureType()
	{
		return furnitureType ;
	}
	
	static void setFurnitureColor(String c)
	{
		furnitureColor = c ;
	}
	static String getFurnitureColor()
	{
		return furnitureColor ;
	}
	
	static void setFurniturePrice(int pri)
	{
		furniturePrice = pri ;
	}
	static int getFurniturePrice()
	{
		return furniturePrice ;
	}
}