class ECommerce
{
   static String eCommerceName ;
   static String product ;
   static String productColor ;
   static int productPrice ;
   
   static void setECommerceName(String name)
   {
      eCommerceName = name ;
   }
   static String getECommerceName()
   {
	   return eCommerceName ;
   }
   
   static void setProduct(String pro)
   {
	   product = pro ;
   }
   static String getProduct()
   {
	   return product ;
   }
   
   static void setProductColor(String color)
   {
	  productColor = color ;
   }
   static String getProductColor()
   {
	   return productColor ;
   }
   
   static void setProductPrice(int p)
   {
	   productPrice = p ;
   }
   static int getProductPrice()
   {
	   return productPrice ;
   }
}