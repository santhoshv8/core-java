class RailwayStation 
{
	static String stationName;
	static String address;
	static String tickatCounter;
	
	static void setStationName(String name) 
	{
		stationName = name;
	}
	static String getStationName() 
	{
		return stationName;
	}
	
	static void setAddress(String add) 
	{
		address = add;
	}
	static String getAddress() 
	{
		return address;
	}
	
	static void setTickatCounter(String tc) 
	{
		tickatCounter = tc ;
	}
    static String getTickatCounter()
	{
		return tickatCounter ;
	}
}
